    @Override
    public void pay(final Context context, IPayEntity entity) {
        this.context = context;
        this.aliPay = (ALiPayEntity) entity;
        // 订单
        String orderInfo = getOrderInfo(aliPay.productName, aliPay.productName, Double.toString(aliPay.fee));

        // 对订单做RSA 签名
        String sign = sign(orderInfo);
        try {
            // 仅需对sign 做URL编码
            sign = URLEncoder.encode(sign, "UTF-8");
        } catch (UnsupportedEncodingException e) {
            e.printStackTrace();
        }

        // 完整的符合支付宝参数规范的订单信息
        final String payInfo = orderInfo + "&sign=\"" + sign + "\"&"
                + getSignType();

        Log.i("支付", "pay: " + orderInfo + " \n" + sign + " \n " + payInfo);
        Runnable payRunnable = new Runnable() {

            @Override
            public void run() {
                // 构造PayTask 对象
                PayTask alipay = new PayTask((Activity) context);

                // 调用支付接口，获取支付结果
                String result = alipay.pay(payInfo, true);
                Log.i("支付", "onpay: " + result);
                Message msg = new Message();
                msg.what = SDK_PAY_FLAG;
                msg.obj = result;
                mHandler.sendMessage(msg);
            }
        };

        // 必须异步调用
        Thread payThread = new Thread(payRunnable);
        payThread.start();
    }


